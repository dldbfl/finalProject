package com.dlms.dto;

import java.util.Date;
/**
 * 
 * @사용목적		:	회원을 참조하는 이미지 테이블	
 * @작성자		:	박진영
 * @작성날짜      :	2020. 6. 1.
 * @마지막수정자	:	박진영
 * @마지막수정일	:	2020. 6. 1.오후 4:14:35
 * @see			:	
 *
 */
public class Member_imgVO {

	private int member_img_no;				// 이미지 번호
	private String member_id;				// 수강생 / 교수
	private int freeboard_no;				// 참고할 자유게시판 번호
	private String member_img_name;			// 자유게시판에 등록될 이미지 / 프로필 사진에 등록될 이미지
	private Date member_img_regdate;		// 등록일자

	public Member_imgVO() {}
	
	public int getMember_img_no() {
		return member_img_no;
	}

	public void setMember_img_no(int member_img_no) {
		this.member_img_no = member_img_no;
	}

	public String getMember_id() {
		return member_id;
	}

	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}

	public int getFreeboard_no() {
		return freeboard_no;
	}

	public void setFreeboard_no(int freeboard_no) {
		this.freeboard_no = freeboard_no;
	}

	public String getMember_img_name() {
		return member_img_name;
	}

	public void setMember_img_name(String member_img_name) {
		this.member_img_name = member_img_name;
	}

	public Date getMember_img_regdate() {
		return member_img_regdate;
	}

	public void setMember_img_regdate(Date member_img_regdate) {
		this.member_img_regdate = member_img_regdate;
	}

	@Override
	public String toString() {
		return "Member_imgVO [member_img_no=" + member_img_no + ", member_id=" + member_id + ", freeboard_no="
				+ freeboard_no + ", member_img_name=" + member_img_name + ", member_img_regdate=" + member_img_regdate
				+ "]";
	}

	public Member_imgVO(int member_img_no, String member_id, int freeboard_no, String member_img_name,
			Date member_img_regdate) {
		super();
		this.member_img_no = member_img_no;
		this.member_id = member_id;
		this.freeboard_no = freeboard_no;
		this.member_img_name = member_img_name;
		this.member_img_regdate = member_img_regdate;
	}

}
