package com.dlms.dto;

import java.util.Date;
/**
 * 
 * @사용목적		:	쪽지함을 참조하는 자료실 테이블	
 * @작성자		:	박진영
 * @작성날짜      :	2020. 6. 1.
 * @마지막수정자	:	박진영
 * @마지막수정일	:	2020. 6. 1.오후 4:16:28
 * @see			:	
 *
 */
public class Message_attachVO {

	private int message_attach_no; 		// 쪽지함 자료실 번호
	private int messenger_no; 				// 발신자 번호
	private String message_attach_filename; // 쪽지함 파일 이름
	private Date message_attach_regdate; 	// 쪽지함 파일 등록일자
	private int receiver_no;			// 수신자 번호
	private String message_attach_path; //파일 경로
	
	public Message_attachVO() {}

	public int getMessage_attach_no() {
		return message_attach_no;
	}

	public void setMessage_attach_no(int message_attach_no) {
		this.message_attach_no = message_attach_no;
	}

	public int getMessenger_no() {
		return messenger_no;
	}

	public void setMessenger_no(int messenger_no) {
		this.messenger_no = messenger_no;
	}

	public String getMessage_attach_filename() {
		return message_attach_filename;
	}

	public void setMessage_attach_filename(String message_attach_filename) {
		this.message_attach_filename = message_attach_filename;
	}

	public Date getMessage_attach_regdate() {
		return message_attach_regdate;
	}

	public void setMessage_attach_regdate(Date message_attach_regdate) {
		this.message_attach_regdate = message_attach_regdate;
	}

	public int getReceiver_no() {
		return receiver_no;
	}

	public void setReceiver_no(int receiver_no) {
		this.receiver_no = receiver_no;
	}

	public String getMessage_attach_path() {
		return message_attach_path;
	}

	public void setMessage_attach_path(String message_attach_path) {
		this.message_attach_path = message_attach_path;
	}

	@Override
	public String toString() {
		return "Message_attachVO [message_attach_no=" + message_attach_no + ", messenger_no=" + messenger_no
				+ ", message_attach_filename=" + message_attach_filename + ", message_attach_regdate="
				+ message_attach_regdate + ", receiver_no=" + receiver_no + ", message_attach_path="
				+ message_attach_path + "]";
	}

	public Message_attachVO(int message_attach_no, int messenger_no, String message_attach_filename,
			Date message_attach_regdate, int receiver_no, String message_attach_path) {
		super();
		this.message_attach_no = message_attach_no;
		this.messenger_no = messenger_no;
		this.message_attach_filename = message_attach_filename;
		this.message_attach_regdate = message_attach_regdate;
		this.receiver_no = receiver_no;
		this.message_attach_path = message_attach_path;
	}
	
}
