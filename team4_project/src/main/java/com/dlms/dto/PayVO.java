package com.dlms.dto;

import java.util.Date;
/**
 * 
 * @사용목적		:	결제 관련 데이터
 * @작성자		:	박진영
 * @작성날짜      :	2020. 6. 1.
 * @마지막수정자	:	민태홍
 * @마지막수정일	:	2020. 6. 15.오후 4:18:56
 * @see			:	
 *
 */
public class PayVO {

	private int pay_no; 							// 결제 내역 번호
	private int lecture_no; 						// 결제 번호
	private String professor_id; 					// 교과목 교수
	private String student_id; 						// 수강생 아이디
	private String class_name; 						// 강의명
	private int pay_price; 							// 금액
	private Date pay_complete_date; 				// 결제일자
	private String pay_method; 						// 결제방법
	private int lecture_regist_no;                  // 수강번호 등록날짜 +인덱스 번호
	
	public PayVO() {}
	
	public int getPay_no() {
		return pay_no;
	}
	public void setPay_no(int pay_no) {
		this.pay_no = pay_no;
	}
	public int getLecture_no() {
		return lecture_no;
	}
	public void setLecture_no(int lecture_no) {
		this.lecture_no = lecture_no;
	}
	public String getProfessor_id() {
		return professor_id;
	}
	public void setProfessor_id(String professor_id) {
		this.professor_id = professor_id;
	}
	public String getStudent_id() {
		return student_id;
	}
	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}
	public String getClass_name() {
		return class_name;
	}
	public void setClass_name(String class_name) {
		this.class_name = class_name;
	}
	public int getPay_price() {
		return pay_price;
	}
	public void setPay_price(int pay_price) {
		this.pay_price = pay_price;
	}
	public Date getPay_complete_date() {
		return pay_complete_date;
	}
	public void setPay_complete_date(Date pay_complete_date) {
		this.pay_complete_date = pay_complete_date;
	}
	public String getPay_method() {
		return pay_method;
	}
	public void setPay_method(String pay_method) {
		this.pay_method = pay_method;
	}
	public int getLecture_regist_no() {
		return lecture_regist_no;
	}
	public void setLecture_regist_no(int lecture_regist_no) {
		this.lecture_regist_no = lecture_regist_no;
	}
	@Override
	public String toString() {
		return "PayVO [pay_no=" + pay_no + ", lecture_no=" + lecture_no + ", professor_id=" + professor_id
				+ ", student_id=" + student_id + ", class_name=" + class_name + ", pay_price=" + pay_price
				+ ", pay_complete_date=" + pay_complete_date + ", pay_method=" + pay_method + ", lecture_regist_no="
				+ lecture_regist_no + "]";
	}
	public PayVO(int pay_no, int lecture_no, String professor_id, String student_id, String class_name, int pay_price,
			Date pay_complete_date, String pay_method, int lecture_regist_no) {
		super();
		this.pay_no = pay_no;
		this.lecture_no = lecture_no;
		this.professor_id = professor_id;
		this.student_id = student_id;
		this.class_name = class_name;
		this.pay_price = pay_price;
		this.pay_complete_date = pay_complete_date;
		this.pay_method = pay_method;
		this.lecture_regist_no = lecture_regist_no;
	}
	
	
}
