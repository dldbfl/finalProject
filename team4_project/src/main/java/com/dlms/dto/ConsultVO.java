package com.dlms.dto;

import java.util.Date;

/**
 * 
 * @사용목적 : 상담문의 게시판
 * @작성자 : 박진영
 * @작성날짜 : 2020. 6. 1.
 * @마지막수정자 : 박진영
 * @마지막수정일 : 2020. 6. 1.오후 3:22:57
 * @see :
 *
 */
public class ConsultVO {

	private int consult_no;// 게시글 번호
	private String consult_title;// 제목
	private String consult_writer;// 수강생
	private String consult_content; // 내용
	private String consult_pwd;// 비밀번호
	private Date consult_regdate;// 등록일자
	private String consult_answer;// 관리자
	private Date consult_answer_date;// 답변날짜
	private int consult_viewcnt;// 조회수
	private String consult_answer_content;// 관리자 답글
	private String consult_type;// 일반글인지 비밀글인지 구분
	private int consult_status;// 답글여부

	public ConsultVO() {}

	public int getConsult_no() {
		return consult_no;
	}

	public void setConsult_no(int consult_no) {
		this.consult_no = consult_no;
	}

	public String getConsult_title() {
		return consult_title;
	}

	public void setConsult_title(String consult_title) {
		this.consult_title = consult_title;
	}

	public String getConsult_writer() {
		return consult_writer;
	}

	public void setConsult_writer(String consult_writer) {
		this.consult_writer = consult_writer;
	}

	public String getConsult_content() {
		return consult_content;
	}

	public void setConsult_content(String consult_content) {
		this.consult_content = consult_content;
	}

	public String getConsult_pwd() {
		return consult_pwd;
	}

	public void setConsult_pwd(String consult_pwd) {
		this.consult_pwd = consult_pwd;
	}

	public Date getConsult_regdate() {
		return consult_regdate;
	}

	public void setConsult_regdate(Date consult_regdate) {
		this.consult_regdate = consult_regdate;
	}

	public String getConsult_answer() {
		return consult_answer;
	}

	public void setConsult_answer(String consult_answer) {
		this.consult_answer = consult_answer;
	}

	public Date getConsult_answer_date() {
		return consult_answer_date;
	}

	public void setConsult_answer_date(Date consult_answer_date) {
		this.consult_answer_date = consult_answer_date;
	}

	public int getConsult_viewcnt() {
		return consult_viewcnt;
	}

	public void setConsult_viewcnt(int consult_viewcnt) {
		this.consult_viewcnt = consult_viewcnt;
	}

	public String getConsult_answer_content() {
		return consult_answer_content;
	}

	public void setConsult_answer_content(String consult_answer_content) {
		this.consult_answer_content = consult_answer_content;
	}

	public String getConsult_type() {
		return consult_type;
	}

	public void setConsult_type(String consult_type) {
		this.consult_type = consult_type;
	}

	public int getConsult_status() {
		return consult_status;
	}

	public void setConsult_status(int consult_status) {
		this.consult_status = consult_status;
	}

	@Override
	public String toString() {
		return "ConsultVO [consult_no=" + consult_no + ", consult_title=" + consult_title + ", consult_writer="
				+ consult_writer + ", consult_content=" + consult_content + ", consult_pwd=" + consult_pwd
				+ ", consult_regdate=" + consult_regdate + ", consult_answer=" + consult_answer
				+ ", consult_answer_date=" + consult_answer_date + ", consult_viewcnt=" + consult_viewcnt
				+ ", consult_answer_content=" + consult_answer_content + ", consult_type=" + consult_type
				+ ", consult_status=" + consult_status + "]";
	}

	public ConsultVO(int consult_no, String consult_title, String consult_writer, String consult_content,
			String consult_pwd, Date consult_regdate, String consult_answer, Date consult_answer_date,
			int consult_viewcnt, String consult_answer_content, String consult_type, int consult_status) {
		super();
		this.consult_no = consult_no;
		this.consult_title = consult_title;
		this.consult_writer = consult_writer;
		this.consult_content = consult_content;
		this.consult_pwd = consult_pwd;
		this.consult_regdate = consult_regdate;
		this.consult_answer = consult_answer;
		this.consult_answer_date = consult_answer_date;
		this.consult_viewcnt = consult_viewcnt;
		this.consult_answer_content = consult_answer_content;
		this.consult_type = consult_type;
		this.consult_status = consult_status;
	}

}
